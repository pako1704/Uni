package iuiuzugzu;

public class MulTableDoWhileLoop {
	    public static void main(String args[]) {
	      int num = 9;
	      int i = 1; // loop variable initialization
	      do {
	         int value = num * i;
	          System.out.println(num + " * " + i + " = " + value);
	          i++;
	      } while ( i <= 10 ); // condition check for exit
	    }

}
